        ('hips_frame', source_properties.get('hips_frame', FALLBACK_SURVEY_METADATA['hips_frame'])),
        ('hips_tile_width', source_properties.get('hips_tile_width', FALLBACK_SURVEY_METADATA['hips_tile_width'])),
